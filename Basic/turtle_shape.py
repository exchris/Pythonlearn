import turtle as t

# 画笔大小
t.pensize(10)
# 画笔颜色
t.pencolor('red')
#
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.mainloop()


